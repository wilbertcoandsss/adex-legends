function hovertext(){
    document.getElementById("hover-text-1").style.color = "red";
    document.getElementById("box-play").style.borderColor = "red";
    document.getElementById("box-play").style.borderWidth = "2";
    document.getElementById("box-play").style.transition = "all .2s";
}

function releaseText(){
    document.getElementById("hover-text-1").style.color = "white";
    document.getElementById("box-play").style.borderColor = "white";
    document.getElementById("box-play").style.borderWidth = "2";
    document.getElementById("box-play").style.transition = "all .2s";
}

